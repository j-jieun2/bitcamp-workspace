package com.eomcs.basic.ex05;


public class Exam0420_test26 {
  public static void main(String[] args) {
        
    int i = 105; // 0b 01101001
    
    System.out.println(i);
    // 00000000 00000000 00000000 01101001
    
    System.out.println(i >> 1);
    // 00000000 00000000 00000000 0110100|1
    // 00000000 00000000 00000000 00110100 = 
    
    System.out.println(i >> 2);
    // 00000000 00000000 00000000 011010|01
    // 00000000 00000000 00000000 00011010 = 26
    
    System.out.println(i >> 3);
    // 00000000 00000000 00000000 01101|001
    // 00000000 00000000 00000000 00001101 = 13
    
    System.out.println(i >> 4);
    // 00000000 00000000 00000000 0110|1001
    // 00000000 00000000 00000000 00000110 = 6
    
  }
}
